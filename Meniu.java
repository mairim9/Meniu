    private void btnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalActionPerformed
        Collection<Alimente> model=Collections.list(modelComanda.elements());
        int total;
        total=model.stream().mapToInt((Alimente p)->p.getPret()).sum();
        fieldTotal.setText(Integer.toString(total));
    }//GEN-LAST:event_btnTotalActionPerformed

    private void rbtnPretActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rbtnPretActionPerformed
        Collection<Alimente> list=Collections.list(modelComanda.elements());
        List<Alimente> arr;
        arr=list.stream().sorted(dupaPret).collect(Collectors.toList());
        refreshModelComanda(arr);
        
    }//GEN-LAST:event_rbtnPretActionPerformed


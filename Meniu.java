    private void refreshModelComanda(List<? extends Alimente> m) {
        modelComanda.clear();
        for (Alimente a : m) {
            modelComanda.addElement(a);
        }
    }
    private void refreshModelOptiuni() {
        //modelMeniu.clear();
        for (Alimente a : meniu) {
            modelMeniu.addElement(a);
        }
    }
